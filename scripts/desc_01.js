function getDescription(opt)
{
switch(opt)
{
   case "1": opt = "Description #1"; break;
   case "2": opt = "Description #2"; break;
   case "3": opt = "Description #3"; break;
   case "4": opt = "Description #4"; break
   default: opt = ""; break;
}
 
 return opt
} //End of getDescription()